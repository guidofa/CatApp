import UIKit

class ColorHelper {
    
    class func neutralGray() -> UIColor {
        return UIColor(displayP3Red: 53/255, green: 53/255, blue: 53/255, alpha: 1)
    }
    
    class func cobaltTurquoise() -> UIColor {
        return UIColor(displayP3Red: 60/255, green: 110/255, blue: 113/255, alpha: 1)
    }
    
    class func whitePC() -> UIColor {
        return UIColor(displayP3Red: 1, green: 1, blue: 1, alpha: 1)
    }
    
    class func frenchGray() -> UIColor {
        return UIColor(displayP3Red: 217/255, green: 217/255, blue: 217/255, alpha: 1)
    }
    
    class func indigoDye() -> UIColor {
        return UIColor(displayP3Red: 40/255, green: 75/255, blue: 99/255, alpha: 1)
    }
}
